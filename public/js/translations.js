// Get the stored language from localStorage or default to Amharic
let currentLanguage = localStorage.getItem('preferredLanguage') || 'am';

// Function to switch language
function switchLanguage(lang) {
    if (!translations[lang]) return;
    
    // Update current language and save to localStorage
    currentLanguage = lang;
    localStorage.setItem('preferredLanguage', lang);
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });

    // Update the language select dropdown
    const languageSelect = document.getElementById('languageSelect');
    if (languageSelect) {
        languageSelect.value = lang;
    }

    // Update the HTML lang attribute
    document.documentElement.lang = lang;
}

// Initialize the language on page load
document.addEventListener('DOMContentLoaded', () => {
    // Set the initial value of the language select
    const languageSelect = document.getElementById('languageSelect');
    if (languageSelect) {
        languageSelect.value = currentLanguage;
    }
    
    // Apply the stored/default language
    switchLanguage(currentLanguage);
});
